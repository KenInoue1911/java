class Main {
  public static void main (String[] args) {
 //trueを用いて真を表す
 System.out.println(true);
 //falseを用いて偽を表す
 System.out.println(false);
 // ==を用いて値を比較した結果を出力
 System.out.println(12/4==3);
 // !=を用いて値を比較した結果を出力
 System.out.println(12/4!=3);
 //変数を定義し値を比較
 boolean bool = 3 * 9 == 27;
 //変数を出力
 System.out.println(bool);
}
}
